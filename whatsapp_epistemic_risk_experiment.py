def compute_metrics(eval_pred):
    """
    Called automatically during training to measure how well
    the model is doing on the validation set.
    """
    logits, labels = eval_pred

    # Convert raw model outputs to 0 or 1 predictions
    predictions = (torch.sigmoid(torch.tensor(logits)) > 0.5).int().numpy()
    labels = labels.astype(int)

    # F1 score — the main metric
    # Micro F1 treats all features equally regardless of how common they are
    f1_micro = f1_score(labels, predictions, average='micro', zero_division=0)
    f1_macro = f1_score(labels, predictions, average='macro', zero_division=0)

    return {
        'f1_micro': round(f1_micro, 4),
        'f1_macro': round(f1_macro, 4),
    }

