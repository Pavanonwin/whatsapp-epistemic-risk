def prepare_dataset(dataframe):
    """Convert a pandas dataframe into a HuggingFace dataset the model can read."""

    dataset = Dataset.from_dict({
        'text': dataframe['text'].tolist(),
        'labels': dataframe[FEATURES].values.astype(float).tolist() # Cast labels to float
    })

    def tokenize_function(batch):
        return tokenizer(
            batch['text'],
            padding='max_length',
            truncation=True,
            max_length=128      # WhatsApp messages are short — 128 tokens is enough
        )

    dataset = dataset.map(tokenize_function, batched=True)
    # Explicitly specify columns to format as torch tensors to avoid VideoReader import issue
    dataset = dataset.with_format('torch', columns=['input_ids', 'attention_mask', 'labels'])

    return dataset

