def score_message(text):
    """
    Takes a WhatsApp message and returns an epistemic risk score.
    """
    inputs = tokenizer(
        text,
        return_tensors='pt',
        padding=True,
        truncation=True,
        max_length=128
    )

    # Move to GPU if available
    device = 'cuda' if torch.cuda.is_available() else 'cpu'
    model.to(device)
    inputs = {k: v.to(device) for k, v in inputs.items()}

    with torch.no_grad():
        outputs = model(**inputs)

    probabilities = torch.sigmoid(outputs.logits).cpu().numpy()[0]
    predictions = (probabilities > 0.5).astype(int)

    composite_score = predictions.sum()

    print("=" * 60)
    print("EPISTEMIC RISK ANALYSIS")
    print("=" * 60)
    print(f"Message: {text[:100]}...")
    print("-" * 60)

    for feature, prob, pred in zip(FEATURES, probabilities, predictions):
        risk_label = "HIGH RISK" if pred == 1 else "low risk"
        bar = "█" * int(prob * 20)
        print(f"{feature:<30} {bar:<20} {prob:.2f}  {risk_label}")

    print("-" * 60)
    print(f"COMPOSITE RISK SCORE: {composite_score}/7")

    if composite_score >= 5:
        verdict = "VERY HIGH RISK — likely misinformation"
    elif composite_score >= 3:
        verdict = "MODERATE RISK — treat with caution"
    else:
        verdict = "LOW RISK — appears more reliable"

    print(f"VERDICT: {verdict}")
    print("=" * 60)

