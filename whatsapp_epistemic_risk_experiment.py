# Create final labels by averaging and rounding
# Where both agree: clear answer. Where they disagree: average = 0.5, rounds to 1 (conservative, high-risk bias)

final_df = pd.DataFrame()
final_df['text'] = df['text']

for feature in FEATURES:
    col_a1 = f'annotator1_{feature}'
    col_a2 = f'annotator2_{feature}'

    # Average the two annotators and round
    # Disagreements (0+1=0.5) round to 1 — erring on side of flagging risk
    final_df[feature] = ((df[col_a1] + df[col_a2]) / 2).round().astype(int)

# Add composite risk score
final_df['risk_score'] = final_df[FEATURES].sum(axis=1)

