kappa_results = {}

for feature in FEATURES:
    # Adjust these column name patterns to match your Excel
    col_a1 = f'annotator1_{feature}'  # e.g. annotator1_source_attribution
    col_a2 = f'annotator2_{feature}'  # e.g. annotator2_source_attribution

